#include "libft.h"

void	*ft_memalloc(size_t size)
{
	void *ptr;

	if (!(ptr = (void *)malloc(sizeof(ptr) * size)) || !size)
		return (NULL);
	ptr = ft_memset(ptr, 0, size);
	return (ptr);
}
